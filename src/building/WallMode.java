package building;

/**
 * Typ wyliczeniowy <code>WallMode</code> reprezentuje wszystkie możliwe stany 
 * ścian. LOOSE oznacza stan luźny (nieprzyczepiony), HORIZONTAL oznacza przyczepienie
 * do haka w poziomie, natomiast VERTICAL oznacza przyczepienie do haka w pionie. 
 * @author olek1
 */
public enum WallMode {
    LOOSE,
    HORIZONTAL,
    VERTICAL;
}
